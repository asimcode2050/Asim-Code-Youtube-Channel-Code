
public class ConvertNumber {
    public static void main(String[] args) {
        int a = -5;
        int b = 5;
        int c = -7;
        int d = 7;
        long result = Integer.toUnsignedLong(a);
        System.out.println(result);
        result = Integer.toUnsignedLong(b);
        System.out.println(result);
        result = Integer.toUnsignedLong(c);
        System.out.println(result);
        result = Integer.toUnsignedLong(d);
        System.out.println(result);
        


    }

}