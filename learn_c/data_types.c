#include <stdio.h>
int main()
{
    int integerVar = 10;
    char charVar = 'A';
    float floatVar = 3.14f;
    double doubleVar = 3.14159;
    printf("Integer: %d\n", integerVar);
    printf("Character: %c\n", charVar);
    printf("Float: %.2f\n", floatVar);
    printf("Double: %.5f\n", doubleVar);
    return 0;
}
